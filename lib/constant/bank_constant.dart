  // All banks including less common ones
  static List<BankData> get allBanks => vietnameseBanks;

  // Search methods
  static BankData? getBankByBIN(String bin) {
    try {
      return vietnameseBanks.firstWhere((bank) => bank.bin == bin);
    } catch (e) {
      return null;
    }
  }

  static BankData? getBankByCode(String code) {
    try {
      return vietnameseBanks.firstWhere(
              (bank) => bank.code.toLowerCase() == code.toLowerCase()
      );
    } catch (e) {
      return null;
    }
  }

  static BankData? getBankByShortName(String shortName) {
    try {
      return vietnameseBanks.firstWhere(
              (bank) => bank.shortName.toLowerCase() == shortName.toLowerCase()
      );
    } catch (e) {
      return null;
    }
  }

  static List<BankData> searchBanks(String query) {
    if (query.isEmpty) return vietnameseBanks;
    final lowerQuery = query.toLowerCase();
    return vietnameseBanks.where((bank) =>
    bank.shortName.toLowerCase().contains(lowerQuery) ||
        bank.name.toLowerCase().contains(lowerQuery) ||
        bank.code.toLowerCase().contains(lowerQuery) ||
        bank.bin.contains(query)
    ).toList();
  }

  // Group banks by type
